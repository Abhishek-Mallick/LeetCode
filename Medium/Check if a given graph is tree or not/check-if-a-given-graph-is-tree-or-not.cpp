class Solution {
  public:
    int isTree(int n, int m, vector<vector<int>> &edges) {
        if(m!=n-1)
            return false;
            
        vector<bool>vis(n,false);
        vector<int>adj[n];
        
        for(int i=0;i<edges.size();i++)
        {
            adj[edges[i][0]].push_back(edges[i][1]);
            adj[edges[i][1]].push_back(edges[i][0]);
        }
        
        int node=0;
        queue<int>q;
    
        vis[node]=true;
        int visited=1;
        q.push(node);
        
        while(q.size())
        {
            int curr=q.front();
            q.pop();
            for(auto it:adj[curr])
            {
                if(!vis[it])
                {
                    vis[it]=true;
                    visited++;
                    q.push(it);
                }
            }
        }
        return (visited==n);
    }
};


