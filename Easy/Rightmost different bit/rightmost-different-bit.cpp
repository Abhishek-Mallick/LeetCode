class Solution
{
    public:
    //Function to find the first position with different bits.
    int posOfRightMostDiffBit(int m, int n)
    {
        bitset<32> num1(m);
        bitset<32> num2(n);
        
        int pos = 1;
        for(int i=0;i<32;i++)
        {
            if(num1[i] == num2[i])
                pos++;
            else
                return pos;
        }
        return -1;
    }
};

